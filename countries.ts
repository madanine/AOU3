// Helper function to get country name by code and language
export const getCountryName = (code: string, lang: 'AR' | 'EN' | 'FR' | 'RU'): string => {
    const country = COUNTRIES.find(c => c.code === code);
    if (!country) return code;
    return lang === 'AR' ? country.name_ar : country.name_en;
};

// Helper function to find country by name (either AR or EN)
export const findCountryByName = (name: string): Country | undefined => {
    return COUNTRIES.find(c =>
        c.name_en.toLowerCase() === name.toLowerCase() ||
        c.name_ar === name
    );
};
